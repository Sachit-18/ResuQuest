@app.route('/feedback', methods=['GET', 'POST'])
def feedback():
    asked_questions = session.get('questions', [])  

    if not asked_questions:
        return render_template('feedback.html', feedback=[], message="No questions were asked.")

    feedback_data = []

    if request.method == 'POST':
        for index, question in enumerate(asked_questions):
            user_answer = request.form.get(f'answer_{index + 1}', "")
            correct_answer = question.get('answer', '')
            question_text = question.get('question', '')

            similarity = check_similarity(correct_answer, user_answer)
            feedback_data.append({
                'question': question_text,
                'user_answer': user_answer,
                'correct_answer': correct_answer,
                'is_correct': similarity >= 0.2,
                'similarity': similarity
            })

    return render_template('feedback.html', feedback=feedback_data)

